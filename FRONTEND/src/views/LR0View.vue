<template>
  <div class="lr0-view">
    <div class="container">
      <header class="view-header">
        <h1>LR0 语法分析器</h1>
        <p class="description">输入上下文无关文法和待分析字符串，查看LR0分析过程</p>
      </header>

      <div class="main-content">
        <!-- 输入区域 -->
        <div class="input-section">
          <div class="input-group">
            <label for="grammar">文法规则</label>
            <textarea
              id="grammar"
              v-model="grammarInput"
              placeholder="请输入文法规则，例如：&#10;S -> A B&#10;A -> a&#10;B -> b"
              rows="8"
              :disabled="store.isLoading"
            ></textarea>
          </div>

          <div class="input-group">
            <label for="input">输入字符串</label>
            <input
              id="input"
              v-model="inputString"
              type="text"
              placeholder="请输入待分析的字符串"
              :disabled="store.isLoading"
            />
          </div>

          <div class="actions">
            <button
              @click="handleParse"
              :disabled="!canParse || store.isLoading"
              class="btn-primary"
            >
              <span v-if="store.isLoading" class="loading-spinner"></span>
              {{ store.isLoading ? '分析中...' : '开始分析' }}
            </button>
            <button
              @click="handleClear"
              :disabled="store.isLoading"
              class="btn-secondary"
            >
              清空
            </button>
          </div>
        </div>

        <!-- 结果区域 -->
        <div class="result-section" v-if="store.hasLR0Result || store.error">
          <!-- 错误提示 -->
          <div v-if="store.error" class="error-message">
            <h3>❌ 错误</h3>
            <p>{{ store.error }}</p>
            <button @click="store.clearError" class="btn-small">关闭</button>
          </div>

          <!-- 成功结果 -->
          <div v-else-if="store.lr0Result" class="result-content">
            <div class="result-header">
              <h3>{{ store.isLR0Success ? '✅ 分析成功' : '❌ 分析失败' }}</h3>
              <p>{{ store.lr0Result.message }}</p>
            </div>

            <!-- 分析步骤 -->
            <div v-if="store.lr0Result.parseSteps?.length" class="parse-steps">
              <h4>分析步骤</h4>
              <ol>
                <li v-for="(step, index) in store.lr0Result.parseSteps" :key="index">
                  {{ step }}
                </li>
              </ol>
            </div>

            <!-- 产生式 -->
            <div v-if="store.lr0Result.productions && Object.keys(store.lr0Result.productions).length" class="productions">
              <h4>使用的产生式</h4>
              <div class="production-list">
                <div
                  v-for="[left, rights] in Object.entries(store.lr0Result.productions)"
                  :key="left"
                  class="production-item"
                >
                  <strong>{{ left }}</strong> →
                  <span v-for="(right, idx) in rights" :key="idx" class="production-right">
                    {{ right }}
                  </span>
                </div>
              </div>
            </div>
          </div>
        </div>

        <!-- 连接状态 -->
        <div class="connection-status">
          <span :class="['status-indicator', store.isConnected ? 'connected' : 'disconnected']">
            {{ store.isConnected ? '🟢 服务器已连接' : '🔴 服务器未连接' }}
          </span>
          <button @click="store.checkConnection" class="btn-small" :disabled="store.isLoading">
            检查连接
          </button>
        </div>
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
import { ref, computed, onMounted } from 'vue'
import { useCompilerStore } from '../stores/compiler'

const store = useCompilerStore()

const grammarInput = ref('')
const inputString = ref('')

// 示例数据
const exampleGrammar = `S -> A B
A -> a
B -> b`

const canParse = computed(() => {
  return grammarInput.value.trim() !== '' && inputString.value.trim() !== ''
})

const handleParse = async () => {
  if (!canParse.value) return

  try {
    await store.parseLR0(grammarInput.value.trim(), inputString.value.trim())
  } catch (error) {
    // 错误已经在store中处理
  }
}

const handleClear = () => {
  grammarInput.value = ''
  inputString.value = ''
  store.clearLR0Result()
}

const loadExample = () => {
  grammarInput.value = exampleGrammar
  inputString.value = 'ab'
}

onMounted(() => {
  // 检查服务器连接
  store.checkConnection()

  // 加载示例
  loadExample()
})
</script>

